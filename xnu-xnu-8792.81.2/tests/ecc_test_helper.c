/*
 * volatile to stop the compiler from optimizing away calls to atan()
 */
volatile double zero = 0.0;

int
main(int argc, char **argv)
{
	void *addr;
	size_t s = sizeof(addr);
	int err;
	int a;

	/*
	 * needs to run as root for sysctl.
	 */
	if (geteuid() != 0) {
		PRINTF("Test not running as root\n");
		exit(-1);
	}

	/*
	 * check for -v for verbose output
	 */
	if (strcmp(argv[1], "-v") == 0) {
		verbose = 1;
	}

	/*
	 * The argument determines what test to try.
	 *  "blahblah" is a test, "Xblahblah" does the test after inject an ECC error.
	 * Tests:
	 * "foo" - invoke a local text function.
	 * "atan" - invoke a shared library text function.
	 * "clean" - read from a clean data page
	 * "dirty" - read from a dirty data page
	 */
	if (strcmp(argv[argc - 1], "foo") == 0) {
		foo();
	} else if (strcmp(argv[argc - 1], "Xfoo") == 0) {
		PRINTF("Warm up call to foo()\n");
		foo();
		addr = ptrauth_strip(&foo, ptrauth_key_function_pointer);
		err = sysctlbyname("vm.inject_ecc", NULL, NULL, &addr, s);
		foo();
	} else if (strcmp(argv[argc - 1], "atan") == 0) {
		PRINTF("atan(0) is %g\n", atan(zero));
	} else if (strcmp(argv[argc - 1], "Xatan") == 0) {
		PRINTF("Warmup call to atan(0) is %g\n", atan(zero));
		addr = ptrauth_strip(&atan, ptrauth_key_function_pointer);
		err = sysctlbyname("vm.inject_ecc", NULL, NULL, &addr, s);
		PRINTF("atan(0) is %g\n", atan(zero));
	} else if (strcmp(argv[argc - 1], "clean") == 0) {
		PRINTF("x.big_data[35] is %d\n", x.big_data[35]);
	} else if (strcmp(argv[argc - 1], "Xclean") == 0) {
		PRINTF("initial read of x.big_data[35] is %d\n", x.big_data[35]);

		addr = &x.big_data[35];
		err = sysctlbyname("vm.inject_ecc", NULL, NULL, &addr, s);
		PRINTF("second read of x.big_data[35] is %d\n", x.big_data[35]);
	} else if (strcmp(argv[argc - 1], "dirty") == 0) {
		x.big_data[35] = (int)random();
		PRINTF("x.big_data[35] is %d\n", x.big_data[35]);
	} else if (strcmp(argv[argc - 1], "Xdirty") == 0) {
		x.big_data[35] = (int)random();
		PRINTF("initial read of dirty x.big_data[35] is %d\n", x.big_data[35]);

		addr = &x.big_data[35];
		err = sysctlbyname("vm.inject_ecc", NULL, NULL, &addr, s);
		PRINTF("second read of x.big_data[35] is %d\n", x.big_data[35]);
	} else if (strcmp(argv[argc - 1], "Xcopyout") == 0) {
		x.big_data[35] = (int)random();
		PRINTF("initial read of dirty x.big_data[35] is %d\n", x.big_data[35]);

		addr = &x.big_data[35];
		err = sysctlbyname("vm.inject_ecc_copyout", NULL, NULL, &addr, s);
		if (err) {
			PRINTF("copyout return %d\n", err);
			exit(err);
		}
		PRINTF("2nd read of dirty x.big_data[35] is %d\n", x.big_data[35]);
	} else if (strcmp(argv[argc - 1], "kernel") == 0) {
		PRINTF("Inducing ECC on kernel page\n");
		addr = (void *)1; /* used to flag some kernel page */
		err = sysctlbyname("vm.inject_ecc", NULL, NULL, &addr, s);
		exit(0);
	} else {
		exit(-1);
	}
	exit(0);
}
